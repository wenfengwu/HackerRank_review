def diagonalDifference(arr):
    # Write your code here
    n = len(arr)
    left = 0
    right = 0
    for i in range(n):
        left += arr[i][i]
    for j in range(n):
        right += arr[j][n-j-1]
    
    return abs(left - right)
    

